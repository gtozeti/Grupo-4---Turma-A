    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Botão adicionar
        
        ModelProduto c = new ModelProduto();
        DaoProduto cdao = new DaoProduto();
        MyUtils u = new MyUtils();
        
       // Validação dos campos 
       if (u.campo(CategoriaComboBox.getSelectedItem().toString()) ||
               u.campo(NomeTextField.getText()) ||
               u.campo(ValorFormattedTextField.getText()) ||
               u.campo(QuantidadeFormattedTextField.getText())){
           JOptionPane.showMessageDialog(null, "Preencha todos os campos!");
       }
       else{
           
                   
        c.setCategoria(CategoriaComboBox.getSelectedItem().toString().strip());
        c.setNome(NomeTextField.getText().strip());
        c.setValor_unit(Double.valueOf(ValorFormattedTextField.getText().replace(',', '.')));
        c.setQuantidade(Integer.valueOf(QuantidadeFormattedTextField.getText()));
       
        //System.out.println(c.getCategoria() + "\n" + c.getNome()+ "\n" + c.getValor_unit() + "\n" + c.getQuantidade());
        
        cdao.create(c);
        
        setVisible(false);
        dispose();
       }
    }//GEN-LAST:event_jButton2ActionPerformed

    // Ação para quando a janela é exibida
    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        
        
        DaoProduto cdao = new DaoProduto();
        jLabel5.setText(cdao.totalOS());
                       
        JComboBox cb = new JComboBox(cdao.categoria().toArray()); //Chamando a função para buscar os valores distintos de categoria
        CategoriaComboBox.setModel(cb.getModel()); //Passando todos os valores para a tela no momento em que a tela é aberta
        cb.insertItemAt("-- Adicione uma categoria --", 0); //Definindo na primeira posição para adicionar um novo produto
        cb.insertItemAt("", 1); 
        CategoriaComboBox.setSelectedIndex(1);
        
        
    }//GEN-LAST:event_formWindowOpened

