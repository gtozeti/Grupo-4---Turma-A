    public void readTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();

        modelo.setNumRows(0);

        TesteTabelaDAO tdao = new TesteTabelaDAO();

        for (TesteTabelaBean t : tdao.read()) {

            modelo.addRow(new Object[]{
                t.getId(),
                t.getNome(),
                t.getIdade(),
                t.getSexo(),
                t.getCidade()
            });
        }
    }

